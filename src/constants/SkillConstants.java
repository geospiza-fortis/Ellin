    public static boolean isFinisherSkill(int skillId) {
        return (
            skillId > Crusader.ComboAttack &&
            skillId < Crusader.ArmorCrash ||
            skillId == 11111002 ||
            skillId == 11111003
        );
    }

    public static boolean isGMSkill(int id) {
        return id > 9000000;
    }

    public static boolean isBeginnerSkill(int skillId) {
        int id = skillId % 10000000;
        return (
            id > 999 &&
            id < 1008 ||
            id >= 1014 &&
            id <= 1019 ||
            id >= 1022 &&
            id <= 1065 ||
            id >= 1069 &&
            id <= 1071
        );
    }

    public static boolean isCorrectJobForSkillRoot(int job, int skillRoot) {
        boolean result;
        if (skillRoot % 100 > 0) {
            result = skillRoot / 10 == job / 10 && job % 10 >= skillRoot % 10;
        } else {
            result = skillRoot / 100 == job / 100;
        }
        return result;
    }

